void	vsSetBassAmp(UINT8 bAmp)
{
	if (bAmp > 15)
	{
		bAmp = 15;
	}
	
	_vs_bassAmp = bAmp;
	vsWriteBass();
}

UINT8	vsGetVolume()
{
	return _vs_volume;
}

void	vsSetVolume(UINT8 volume)
{
	if (volume > 100)
	{
		volume = 100;
	}
	
	UINT16 vol = 0x0000;
	_vs_volume = volume;
	
	vol = 100 - _vs_volume;
	
	vsWriteReg(VS_SCI_VOL, ((vol<<8)|(vol)));
}

UINT8	vsCheckDreq()
{
	return (VS_DREQ())?0xFF:0x00;
}

UINT8	vsData(UINT8 *data)
{
	int i = 0;
	VS_XDCS_ENABLE();
	for(i = 0; i < 32; i++)
	{
		spi_write(*data);
		data++;
	}
	VS_XDCS_DISABLE();
	
	return 0;
}

void	vsCancel()
{
	UINT8 tmp = 0;
	tmp = vsReadReg(VS_SCI_MODE);
	tmp = tmp|(VS_SM_CANCEL);
	vsWriteReg(VS_SCI_MODE, tmp);
}

