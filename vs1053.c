void	vsPutZeros(UINT16 count)
{
	spi_2x_clr();
	VS_XDCS_ENABLE();
	while(count--)
	{
		SPDR = 0x00;
		wait_spi();
	}
	VS_XDCS_DISABLE();
	spi_2x();
}